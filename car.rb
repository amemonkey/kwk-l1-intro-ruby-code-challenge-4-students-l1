class Car
  def initialize(make, model, color)
    @make = make
    @model = model
    @color = color
  end
  
  def make
    @make
  end
  
  def make = (new_make)
    @make = new_make
  
  def model
    @model
  end
  
  def model = (new_model)
    @model = new_model
  end
  
  def color
    @color
  end
  
  def color = (new_color)
    @color = new_color
  end
  
end